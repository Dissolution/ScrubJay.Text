namespace ScrubJay.Text;

/// <summary>
/// The direction that text is read and/or processed
/// </summary>
public enum TextDirection
{
    /// <summary>
    /// <c>(default)</c> Left to Right
    /// </summary>
    LeftToRight = 0,
        
    /// <summary>
    /// Right to Left
    /// </summary>
    RightToLeft = 1,
}
